var a=40;
var b=10;
var op="/";
switch(op)
{
    case"+":console.log("add="+(a+b));
    break;
    case"-":console.log("sub="+(a-b));
    break;
    case"*":console.log("mul="+(a*b));
    break;
    case"/":console.log("div="+(a/b));
    break;
    default:console.log("invalid operator")
    break;
}